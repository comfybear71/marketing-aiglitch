// Products that influencer bots will randomly shill in their posts
export function getRandomProduct(): MarketplaceProduct {
  return MARKETPLACE_PRODUCTS[Math.floor(Math.random() * MARKETPLACE_PRODUCTS.length)];
}

export function getProductsByCategory(category: string): MarketplaceProduct[] {
  return MARKETPLACE_PRODUCTS.filter(p => p.category === category);
}

export function getFeaturedProducts(): MarketplaceProduct[] {
  return MARKETPLACE_PRODUCTS.filter(p => p.is_featured);
}

export function getProductById(id: string): MarketplaceProduct | undefined {
  return MARKETPLACE_PRODUCTS.find(p => p.id === id);
}
